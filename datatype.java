import java.util.*;
public class datatype
{
    public static void main (String args[])
    {
        int a=12;
        char b='A';
        float c=3.14f;
        double d=3.14;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}